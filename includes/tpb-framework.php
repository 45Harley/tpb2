<div class="tpb-framework">
    <p class="framework-intro">For 250 years, certain truths have held. Not because they were written, but because they are self-evident. All are created equal. Government by consent. No one above the law. These are not wishes. They are principles — the rock upon which the republic stands.</p>
    
    <p>But principles require people. And people, scattered, are just voices in the wind.</p>
    
    <p class="framework-lens">The People's Branch is the lens.</p>
    
    <p>Here, individual voices become civil debate. Civil debate becomes unified thought. Unified thought becomes unified ideas. Unified ideas become unified action.</p>
    
    <p class="framework-physics">This is the physics of democracy: rays of truth, gathered into focus, bring light.</p>
    
    <p>The principles do not change. They are the gauge, the guide, the moderator of every conversation. They measure our progress. They hold us accountable to our highest aspirations.</p>
    
    <p>We are not here to shout. We are here to debate — civilly, purposefully, together. To close the gap between who we say we are and who we have yet to become.</p>
    
    <p>The path to a more perfect union is not behind us. It is underfoot, with every step we take together.</p>
    
    <p class="framework-cta"><em>If you are here, you have a voice. Use it.</em></p>
